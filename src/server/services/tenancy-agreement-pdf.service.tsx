function toSafeText(value: string) {
  return value.replace(/\u20A6/g, "NGN ");
}

function splitBody(value: string) {
  return toSafeText(value)
    .split("\n")
    .map((line) => line.trimEnd());
}

function AgreementPdfDocument({
  agreement,
}: {
  agreement: TenancyAgreementDocumentRow;
}) {
  const acceptedAt = agreement.tenant_accepted_at
    ? new Intl.DateTimeFormat("en-NG", {
        dateStyle: "medium",
        timeStyle: "short",
      }).format(new Date(agreement.tenant_accepted_at))
    : "Not accepted yet";

  const body = agreement.finalized_body || agreement.agreement_body;

  return (
    <Document
      title={agreement.title}
      author="Tenuro"
      subject="Tenancy Agreement"
    >
      <Page size="A4" style={styles.page}>
        <View style={styles.header}>
          <Text style={styles.brand}>Tenuro</Text>
          <Text style={styles.title}>{agreement.title}</Text>
          <Text style={styles.meta}>
            Agreement ID: {agreement.id} | Status: {agreement.document_status} |
            Accepted: {acceptedAt}
          </Text>
        </View>

        <View style={styles.section}>
          {splitBody(body).map((line, index) => (
            <Text key={`${line}-${index}`} style={styles.paragraph}>
              {line || " "}
            </Text>
          ))}
        </View>

        <Text style={styles.footer}>
          Generated by Tenuro. This copy reflects the saved agreement record and
          tenant digital acceptance status at the time of generation.
        </Text>
      </Page>
    </Document>
  );
}

function createPdfPath(agreement: TenancyAgreementDocumentRow) {
  return [
    agreement.landlord_id,
    agreement.tenant_id,
    agreement.tenancy_id,
    `${agreement.id}.pdf`,
  ].join("/");
}

export async function generateAndStoreTenancyAgreementPdf(
  supabase: SupabaseClient,
  agreement: TenancyAgreementDocumentRow,
) {
  if (
    agreement.document_status !== "sent_to_tenant" &&
    agreement.document_status !== "accepted"
  ) {
    throw new AppError(
      "AGREEMENT_PDF_NOT_READY",
      "Finalize the agreement before generating a PDF.",
      400,
    );
  }

  const pdfBuffer = await renderToBuffer(
    <AgreementPdfDocument agreement={agreement} />,
  );

  const pdfPath = createPdfPath(agreement);

  await uploadTenancyAgreementPdf({
    path: pdfPath,
    pdfBuffer,
  });

  return updateTenancyAgreementPdfPath(supabase, {
    agreementId: agreement.id,
    pdfPath,
  });
}
