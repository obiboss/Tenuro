import "server-only";

import {
  Document,
  Page,
  StyleSheet,
  Text,
  View,
  renderToBuffer,
} from "@react-pdf/renderer";
import { AppError } from "@/server/errors/app-error";
import type { TenancyAgreementDocumentRow } from "@/server/repositories/tenancy-agreements.repository";
import { updateTenancyAgreementPdfPath } from "@/server/repositories/tenancy-agreements.repository";
import type { SupabaseClient } from "@supabase/supabase-js";
import { uploadTenancyAgreementPdf } from "@/server/services/storage.service";

